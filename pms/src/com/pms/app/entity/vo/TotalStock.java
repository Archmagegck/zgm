package com.pms.app.entity.vo;

public class TotalStock {
	
	private String styleName;
	
	private String pledgePurityName;
	
	private Double specWeight;
	
	private String storage;
	
	private Double amount;
	
	private Double sumWeight;
	
	private Double sumValue;

	public String getStyleName() {
		return styleName;
	}

	public void setStyleName(String styleName) {
		this.styleName = styleName;
	}

	public String getPledgePurityName() {
		return pledgePurityName;
	}

	public void setPledgePurityName(String pledgePurityName) {
		this.pledgePurityName = pledgePurityName;
	}

	public Double getSpecWeight() {
		return specWeight;
	}

	public void setSpecWeight(Double specWeight) {
		this.specWeight = specWeight;
	}

	public String getStorage() {
		return storage;
	}

	public void setStorage(String storage) {
		this.storage = storage;
	}

	public Double getAmount() {
		return amount;
	}

	public void setAmount(Double amount) {
		this.amount = amount;
	}

	public Double getSumWeight() {
		return sumWeight;
	}

	public void setSumWeight(Double sumWeight) {
		this.sumWeight = sumWeight;
	}

	public Double getSumValue() {
		return sumValue;
	}

	public void setSumValue(Double sumValue) {
		this.sumValue = sumValue;
	}
	
	
}
